void display_welcome(unsigned char *gImage)
{
        int i=0,j=0;
        unsigned char *p=gImage;
        int hg=32,wd=97,bs=1;
        int tem=154;

{
  
        system("clear");
        printf("\n");
        for(i=0;i<hg;)
        {
                for(j=0;j<wd;j++)
                {
                        if(*p>=tem)printf(" ");
                        else printf("\033[44;37m#\033[0m");//printf(“\033[40;37msomthe here\n\033[0m”);
                        p++;
                }
                usleep(20000);
                i+=bs;
                p=gImage+i*wd;
        printf("\n");
        }
        p=gImage;
}
        return;
}

